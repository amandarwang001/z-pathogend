            <p>My career in the world of national and international high-level disinfection had heightened my awareness of the very real dangers
                of hospital-acquired infections, but didn’t quite prepare me. </p>
            <p>My experiences were showing me that products on the infection prevention and control market didn’t adequately address the needs of
                many settings and situations. The sizes, the costs, the unreliability, the toxic chemicals trading one issue for another, were all
                highlighting the inadequacies inherent in battling a rapidly emerging threat. Based on the factors I was witnessing and feedback from
                customers, we sought to create something to fill those gaps. </p>
            <p>As we were working on our design model, our oldest daughter (a senior in high school) who had been diagnosed with a cholesteatoma
                and had undergone some pretty serious surgeries, began to experience increased pain, loss of balance, headaches, weakness, and weight
                loss. No doctor (and there were plenty of doctors) could isolate the cause. </p>
            <p>My search was on. That’s what mothers and fathers do for their children. Find the answer. Fix it.  I became a sponge for any tidbit
                of information, the latest research that might fit together to lead me to an answer: Even a direction forward. </p>
            <p>After months of research and tests, we finally received a diagnosis. A resistant staph infection had infiltrated her inner ear canal
                and exacerbated her issue. This staph diagnosis led to multiple rounds of antibiotics and began months of getting a little better only to
                have chronic ear infection symptoms return with a vengeance. </p>
            <p>As we worked to help her regain her health, one nagging question remained with me: How, where had she been exposed to this staph? Finally,
                we narrowed it down; she had contracted it in the weight room where she worked out each week. </p>
            <p>The guilt and frustration was almost overwhelming. I should have seen what was happening. That’s my field! I see it everyday! It would have
                been as simple as disinfecting the space where she been working out. I’d let her down.  That very hard realization pushed me forward toward
                the next stage of our journey. </p>
            <br><br>
            <h5 class="gray-text"><strong>Focus on the rise of Community Acquired Infections </strong></h5>
            <p>For years, I acknowledged the inevitability of drug resistant infections in our community settings,, but was struck by the prevalence of MDRO’s
                in schools, buses, airplanes, etc. </p>
            <p>I was intent on making an impact so as to spare others the horror we experienced: It was apparent that infection prevention and control is not a
                one-and-done challenge we face.   This invisible threat to our children and ourselves would be an issue we’d face for decades to come. </p>
            <p>As I’ve shared our experience with others, I am struck by just how often resistant infections are affecting families, resulting too often in
                life-long debilitation and even deadly sepsis. </p>
            <br><br>
            <h5 class="gray-text"><strong>Moving forward with a bigger mission in mind. </strong></h5>
            <p>Drawing on my years of experience, I was compelled to create a service company based on the philosophy that protocol, equipment, and trained staff
                would combine with a passion for saving lives by providing decontamination for the community setting. Janitorial staff and often
                Environmental Services simply are not equipped to combat the issues infectious diseases present. As our vision grew, we brought in Paramedics
                , Infection Control professionals, biomedical professionals and many others who have dedicated themselves to our philosophy and have become
                Pathogend<sup><i class="far fa-registered" style="font-size: 14px;"></i></sup> Service owners around the country. Together, each day we strive to educate and create a better understanding of the challenges
                facing infection prevention and control in the spaces of our lives. </p>
            <br><br>
