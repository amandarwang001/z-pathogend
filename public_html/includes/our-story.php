            <p>
                My career in the world of national and international high-level disinfection had heightened my awareness
                of the very real dangers of hospital-acquired infections, but didn't quite prepare me.
                <br><br>
                My experiences were showing me that products on the infection prevention and control market didn't
                adequately address the needs of many settings and situations. The sizes, the costs, the unreliability,
                the toxic chemicals trading one issue for another, were all highlighting the inadequacies inherent in
                battling a rapidly emerging threat. Based on the factors I was witnessing and feedback from customers,
                we sought to create something to fill those gaps.
                <br><br>
                As we were working on our design model, our oldest daughter Gabriella (a senior in high school) was
                diagnosed with a cholesteatoma and had undergone some pretty serious surgeries. During her recovery, she
                began to experience increased pain, loss of balance, headaches, weakness, and weight loss. No doctor
                (and there were plenty of doctors) could isolate the cause.
                <br><br>
                After months of research and tests, we finally received a diagnosis. A resistant staph infection had
                infiltrated her inner ear canal. This diagnosis led to multiple rounds of antibiotics, hospitalizations,
                and months of getting a little better only to have the infection return with a vengeance.
                <br><br>
                As we worked to help her regain her health, one nagging question remained with me: How and where had she
                been exposed to this resistant organism? Finally, we narrowed it down; she had contracted it in the
                weight room where she worked out each week.
                <br><br>
                I should have seen the potential threat for community-acquired infections. It would have been as simple
                as disinfecting the space where she been working out. I'd let her down. That very hard realization
                pushed me forward toward the next stage of our journey.
            </p>


            <h5 class="gray-text">Focus on the rise of Community Acquired Infections</h5>
            <p>I was intent on making an impact so as to provide a solution. It was apparent that infection prevention
                and control is not a one-and-done challenge we face. This invisible threat to our families and ourselves
                would be an issue we'd face for decades to come.
            </p>

            <h5 class="gray-text">Creating a Service</h5>
            <p>As our vision grew, we concluded there was not an educated provider people to rely on to help them
                overcome the emerging resistant germs. So we created one. We brought in Paramedics , Infection Control
                professionals, biomedical professionals and many others who have dedicated themselves to our philosophy
                and have become Pathogend® Certified Providers around the country. Together, each day we strive to
                educate and create a better understanding of the challenges facing infection prevention and control in
                the spaces of our lives.</p>
